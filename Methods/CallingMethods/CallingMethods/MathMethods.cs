namespace MathApp
{
    public class MathMethods
    {
        // Method to perform addition operation on the received parameter
        public int Add(int number)
        {
            int result = number + 10;
            return result;
        }

        // Method to perform subtraction operation on the received parameter
        public int Subtract(int number)
        {
            int result = number - 5;
            return result;
        }

        // Method to perform multiplication operation on the received parameter
        public int Multiply(int number)
        {
            int result = number * 2;
            return result;
        }
    }
}
